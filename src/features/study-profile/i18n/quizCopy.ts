// Helper to get localized text by key
export function getLocalizedText(key: string, locale: 'vi' | 'en' = 'vi'): string {
  const copy = getQuizCopy(locale);
  const keys = key.split('.');
  let value: any = copy;
  
  for (const k of keys) {
    value = value?.[k];
  }
  
  return value || key;
}
